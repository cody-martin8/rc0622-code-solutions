//   expression that evaluates whether character is vowel, if so return true, else return false
//     use comparison for expression for each vowel, upper and lower case

function isVowel(char) {
  if (char === 'a' || char === 'e' || char === 'i' || char === 'o' || char === 'u' || char === 'A' || char === 'E' || char === 'I' || char === 'O' || char === 'U') {
    return true;
  } else {
    return false;
  }
}

